public class ExampleMethodsStatic {
    public static void printName(String name) {
        System.out.println("Hello, my name is " + name + "!");
    }

    public static void favAnimal(String animal) {
        System.out.println("My favorite animal is " + animal + ".");
    }

    public static void favNum(int num) {
        System.out.println("My favorite number is " + num + ".");
    }
}